    <h2 class="mt-4">2. Оптимизация контента</h2>
    <p>Добавьте ключевые слова в заголовки, мета-описания и текст. Например, в заголовке страницы используйте "Купить
        цветы в Ташкенте недорого".</p>

    <h2 class="mt-4">3. Техническое SEO</h2>
    <p>Убедитесь, что ваш сайт загружается быстро (менее 3 секунд). Проверьте скорость через Google PageSpeed Insights.
